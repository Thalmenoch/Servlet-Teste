package bean;

public class Texto {
	private String dest;
	private String ass;
	private String mnsg;
	
	public String getDest() {
		return dest;
	}
	public void setDest(String dest) {
		this.dest = dest;
	}
	public String getAss() {
		return ass;
	}
	public void setAss(String ass) {
		this.ass = ass;
	}
	public String getMnsg() {
		return mnsg;
	}
	public void setMnsg(String mnsg) {
		this.mnsg = mnsg;
	}
	
	
}
